package Page22.Task2235;

public class AddTwoIntegers {
    public static void main(String[] args) {
        int num1 = -10;
        int num2 = 4;

        System.out.println(new AddTwoIntegers().sum(num1, num2));
    }
    public int sum(int num1, int num2) {
        return num1 + num2;
    }
}
